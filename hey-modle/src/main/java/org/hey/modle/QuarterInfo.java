package org.hey.modle;

public class QuarterInfo {
	//所属季度
	private String quarter;
	//该季度余额
	private String quarterBalance;
	public String getQuarter() {
		return quarter;
	}
	public void setQuarter(String quarter) {
		this.quarter = quarter;
	}
	public String getQuarterBalance() {
		return quarterBalance;
	}
	public void setQuarterBalance(String quarterBalance) {
		this.quarterBalance = quarterBalance;
	}
	
	
}
